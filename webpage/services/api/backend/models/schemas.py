"""Pydantic data models for the Paulie's Prediction Partners Kalshi trading system.

Models mirror Kalshi API v2 data structures (using FixedPointDollars ``str``
format for monetary fields) and add internal agent/risk types.
"""

from __future__ import annotations

from datetime import datetime
from decimal import Decimal
from typing import Literal, Optional

from pydantic import BaseModel, ConfigDict, Field


# ---------------------------------------------------------------------------
# Kalshi API models
# ---------------------------------------------------------------------------


class Market(BaseModel):
    """Kalshi market data."""

    model_config = ConfigDict(populate_by_name=True)

    ticker: str
    event_ticker: str
    series_ticker: str = ""
    market_type: Literal["binary", "scalar"]
    category: str = ""
    yes_sub_title: str = ""
    no_sub_title: str = ""
    status: Literal[
        "initialized",
        "inactive",
        "active",
        "closed",
        "determined",
        "disputed",
        "amended",
        "finalized",
    ]

    # Prices — FixedPointDollars strings (e.g. "0.5600")
    yes_bid_dollars: str = ""
    yes_ask_dollars: str = ""
    no_bid_dollars: str = ""
    no_ask_dollars: str = ""
    last_price_dollars: str = ""

    # Volume / interest
    volume_fp: str = ""
    volume_24h_fp: str = ""
    open_interest_fp: str = ""

    # Timestamps
    open_time: datetime
    close_time: datetime
    created_time: datetime

    # Misc
    notional_value_dollars: str = ""
    result: Optional[str] = None


class Fill(BaseModel):
    """A single executed fill returned by the Kalshi API."""

    model_config = ConfigDict(populate_by_name=True)

    fill_id: str
    trade_id: str
    order_id: str
    ticker: str
    side: Literal["yes", "no"]
    action: Literal["buy", "sell"]
    count_fp: str
    yes_price_fixed: str
    no_price_fixed: str
    is_taker: bool
    fee_cost: str
    created_time: datetime


class Order(BaseModel):
    """An order as returned by the Kalshi API."""

    model_config = ConfigDict(populate_by_name=True)

    order_id: str
    ticker: str
    status: Literal["resting", "canceled", "executed"]
    side: Literal["yes", "no"]
    yes_price_dollars: str
    fill_count_fp: str
    remaining_count_fp: str
    initial_count_fp: str
    client_order_id: str = ""
    created_time: datetime
    last_update_time: datetime
    expiration_time: Optional[datetime] = None


class Position(BaseModel):
    """A portfolio position on a single market."""

    model_config = ConfigDict(populate_by_name=True)

    ticker: str
    market_exposure: str
    position: str
    realized_pnl_dollars: str
    total_traded_fp: str


class OrderRequest(BaseModel):
    """Payload for creating a new order on Kalshi."""

    model_config = ConfigDict(populate_by_name=True)

    ticker: str
    client_order_id: str = Field(default="")
    side: Literal["yes", "no"]
    action: Literal["buy", "sell"]
    count_fp: str
    type: Literal["limit", "market"]
    yes_price_dollars: Optional[str] = None
    no_price_dollars: Optional[str] = None
    expiration_time: Optional[datetime] = None


class Balance(BaseModel):
    """Account balance snapshot."""

    model_config = ConfigDict(populate_by_name=True)

    balance: Decimal
    portfolio_value: Decimal


class Candlestick(BaseModel):
    """A single OHLCV candlestick bar."""

    model_config = ConfigDict(populate_by_name=True)

    end_period_ts: int
    open: Optional[str] = None
    high: Optional[str] = None
    low: Optional[str] = None
    close: Optional[str] = None
    volume: str = ""
    mean: Optional[str] = None


class Event(BaseModel):
    """A Kalshi event containing one or more markets."""

    model_config = ConfigDict(populate_by_name=True)

    event_ticker: str
    series_ticker: str
    title: str
    category: str
    sub_title: str = ""
    markets: Optional[list[Market]] = None


class Series(BaseModel):
    """A Kalshi event series."""

    model_config = ConfigDict(populate_by_name=True)

    series_ticker: str
    title: str
    category: str
    tags: Optional[list[str]] = None


# ---------------------------------------------------------------------------
# Internal agent / risk models
# ---------------------------------------------------------------------------


class AgentState(BaseModel):
    """Runtime state for an autonomous trading agent."""

    model_config = ConfigDict(populate_by_name=True)

    agent_name: str
    mode: Literal["auto", "semi-auto", "safe"]
    status: str
    total_trades: int = 0
    win_count: int = 0
    loss_count: int = 0
    realized_pnl: Decimal = Decimal("0")
    last_decision_time: Optional[datetime] = None


class TradeIntent(BaseModel):
    """A proposed trade generated by an agent awaiting approval or execution."""

    model_config = ConfigDict(populate_by_name=True)

    agent_name: str
    ticker: str
    side: Literal["yes", "no"]
    action: Literal["buy", "sell"]
    count_fp: str
    price_dollars: str
    reasoning: str
    pattern_detected: Optional[str] = None


class RiskEvent(BaseModel):
    """An event recorded by the risk-management subsystem."""

    model_config = ConfigDict(populate_by_name=True)

    event_type: str
    agent_name: str
    ticker: str
    reason: str
    timestamp: datetime
